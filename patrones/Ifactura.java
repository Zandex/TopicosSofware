    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // BOTON CARGAR
        selectrow=out_Table.getSelectedRow();
        Object [] fila=new Object[10];
        if (selectrow>=0) {          
            DefaultTableModel defaultTableModel = (DefaultTableModel) out_Table.getModel();            
            fila=new Object[defaultTableModel.getColumnCount()];  
            for (int i = 0; i < fila.length; i++) {
                fila[i]=defaultTableModel.getValueAt(selectrow, i);
            }
        }
        idtemp=Integer.valueOf(fila[0].toString());
        in_id.setText(fila[0].toString());
        in_fecha.setText(fila[1].toString());
        ArrayList<Cliente> list= db.getClienttable().clientes;        
        for(Cliente client: list){                
            if (fila[2].toString().equalsIgnoreCase(client.getNombre())) {
                in_cliente.setText(String.valueOf(client.getId()));
            }
        }
        in_totalfactura.setText(fila[3].toString());
        
    }//GEN-LAST:event_jButton3ActionPerformed

