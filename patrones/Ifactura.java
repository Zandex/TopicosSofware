    public void add(Object [] fila){
        DefaultTableModel modelo = (DefaultTableModel) out_Table.getModel();
        modelo.addRow(fila);
        out_Table.setModel(modelo);
        in_id.setText("");
        in_fecha.setText("");
        in_cliente.setText("");
        in_totalfactura.setText("");
    }
    
    public void cleartable(){        
        DefaultTableModel defaultTableModel = (DefaultTableModel) out_Table.getModel();
        while ( 0 < out_Table.getRowCount()) {
            defaultTableModel.removeRow(0);
        }
        fill();
    }
    
    public void fill(){
        ArrayList<Factura> list= db.getFacturatable().facturas;
        
        for(Factura factu: list){                
            Object [] fila={factu.getNroFactura(),factu.getFechaFactura(),factu.getCliente().getNombre(),factu.getTotalFactura(),factu.getEstado(),factu.getItems().size()};
            add(fila);
        }
    }
    
    public void fill_boxitems(){
        ArrayList<Item> list= db.getItemtable().items;
        
        in_bItems.removeAllItems();
        for(Item items: list){                
            in_bItems.addItem(items.getDescripcion());
        }
    }
    public void fill_items(){
        DefaultTableModel defaultTableModel = (DefaultTableModel) out_tableitems.getModel();
        while ( 0 < out_tableitems.getRowCount()) {
            defaultTableModel.removeRow(0);
        }
        
        ArrayList<Item> list= db.getItemtable().items;        
        Object [] fila=new Object[2];
        DefaultTableModel modelo = (DefaultTableModel) out_tableitems.getModel();
        
         
        in_bItems.removeAllItems();
        for(Item items: listitems){                
            fila[0]=items.getDescripcion();
            fila[1]=items.getValorUnidad();
            modelo.addRow(fila);
        }
    }
    public void searchcliente(int ida){
        ArrayList<Cliente> list= db.getClienttable().clientes;
        
        for(Cliente client: list){                
            if (ida==client.getId()) {
                Usuario=client;
            }
        }
    }
    
    public void additem(){
        ArrayList<Item> list= db.getItemtable().items;
        Object [] fila=new Object[2];
        DefaultTableModel modelo = (DefaultTableModel) out_tableitems.getModel();
        
        for(Item item: list){                
            if (item.getDescripcion().equalsIgnoreCase(in_bItems.getSelectedItem().toString())) {
                listitems.add(item);
                fila[0]=item.getDescripcion();
                fila[1]=item.getValorUnidad();
                modelo.addRow(fila);
            }
        }
        
    }
  

